package helloworld;

public class MethodsFunctions {

	public static void main(String[] args) {
		
	
	
		
		
	sayHello();
	sayHi();
	greet();

	
	}

	static void sayHello() {
		System.out.println("Hello");
	
	}
	
	static void sayHi() {
		System.out.println("Hi");
		
	}
	//LOCAL VARIABLE
	static void greet() {
		String greetings = "Hello BAKA!!";
		System.out.println(greetings);
	}
	
}
