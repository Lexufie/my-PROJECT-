package helloworld;

public class fORLOOP {

	public static void main(String[] args) {

		
		for(int i = 0;i < 5;i++) {
		System.out.println(i);
		
		int x = 0;
		while(x < 5) {
			System.out.println(x);
			x++;
			
		}
		
		for(int l = 0;l <5;l++) {
			System.out.println(l);
			
			
			
		}
		}
		
		
		
	}
}

